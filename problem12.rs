fn arrangements(s: String, lengths: &Vec<usize>) -> usize {
    let mut types : Vec<usize> = Vec::new();
    let dim1 = s.len(); // how many characters you have processed
    let dim2 = lengths.len(); // how many lengths you have completed
    for c in s.chars() {
        match c {
            '?' => types.push(0),
            '.' => types.push(1),
            '#' => types.push(2),
            _ => {},
        }
    }
    let mut count: Vec<Vec<usize>> = Vec::new();
    for _ in 0..dim1+1 {
        count.push(vec![0;dim2+1]);
    }
    count[0][0] = 1;
    for i in 1..dim1+1 {
        for j in 0..dim2+1 {
            // base case. there you can just take the solution from the last length
            if types[i-1] != 2 {
                count[i][j] = count[i-1][j];
            }
            if j == 0 { // you aren't actually able to create a length
                continue;
            }
            // create the length
            let cur = lengths[j-1];
            if cur == i { // can create in exactly 
                for k in 0..i {
                    if types[k] == 1 {
                        break;
                    }
                    if k == i-1 {
                        count[i][j] += count[0][j-1];
                    }
                }
            } else if i > cur { // further need a valid at the end
                for k in i-cur-1..i { // i = 2, cur = 1, i = 5, cur = 1
                    if k == i-cur-1 {
                        if types[k] == 2 {
                            break
                        }
                    } else if types[k] == 1 {
                        break
                    }
                    if k == i-1 {
                        count[i][j] += count[i-cur-1][j-1];
                    }
                }
            }
        }
    }
    return count[dim1][dim2];
 }

